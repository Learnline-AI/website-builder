// Helper functions
export function getComponentsByZone(zoneId: string): ComponentEntry[] {
  return componentRegistry.filter(c => c.zone === zoneId)
}

export function getComponentsByCategory(categoryId: string): ComponentEntry[] {
  return componentRegistry.filter(c => c.categories.includes(categoryId))
}

export function getComponentById(id: string): ComponentEntry | undefined {
  return componentRegistry.find(c => c.id === id)
}

export function searchComponents(query: string): ComponentEntry[] {
  const lower = query.toLowerCase()
  return componentRegistry.filter(c =>
    c.name.toLowerCase().includes(lower) ||
    c.description.toLowerCase().includes(lower) ||
    c.tags.some(t => t.toLowerCase().includes(lower))
  )
}
